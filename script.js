/* ----------Script for Select Batch---------- */
function select(value){
    if(value == "bca1821"){
        document.getElementById("bca818").style.display="block";
        document.getElementById("bca819").style.display="none";
        document.getElementById("bca820").style.display="none";
        document.getElementById("tr").style.display="block";
    }
    if(value == "bca1922"){
        document.getElementById("bca818").style.display="none";
        document.getElementById("bca819").style.display="block";
        document.getElementById("bca820").style.display="none";
        document.getElementById("tr").style.display="block";
    }
    if (value =="bca2023"){
        document.getElementById("bca818").style.display="none";
        document.getElementById("bca819").style.display="none";
        document.getElementById("bca820").style.display="block";
        document.getElementById("tr").style.display="block";
    }
    if(value == "null"){
        document.getElementById("bca818").style.display="none";
        document.getElementById("bca819").style.display="none";
        document.getElementById("bca820").style.display="none";
        document.getElementById("tr").style.display="none";
    }
}
